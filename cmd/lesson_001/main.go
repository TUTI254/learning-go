package main

import "fmt"

func main() {
	// variables and types
	var num int = 10
	fmt.Println(num)

	var notJustNum float64 = 10.5
	fmt.Println(notJustNum)

	var normString string = "Hello World"
	fmt.Println(normString)

	var isTrue bool = true
	fmt.Println(isTrue)

	// constants
	const pi float64 = 3.14159265358979323846
	fmt.Println(pi)

	const myName string = "Evan"
	fmt.Println(myName)

	const myAge int = 25
	fmt.Println(myAge)

}
